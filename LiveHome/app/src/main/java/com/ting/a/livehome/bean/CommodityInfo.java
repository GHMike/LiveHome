package com.ting.a.livehome.bean;

public class CommodityInfo {

    private String CommodityID;
    private String CommodityName;
    private String CommoditySize;
    private String CommodityPrice;
    private String CommodityTotalPrice;
    private String CommodityType;
    private String CommoditySupplier;
    private String SupplierDetail;

    public String getSupplierDetail() {
        return SupplierDetail;
    }

    public void setSupplierDetail(String supplierDetail) {
        SupplierDetail = supplierDetail;
    }

    public String getCommodityID() {
        return CommodityID;
    }

    public void setCommodityID(String commodityID) {
        CommodityID = commodityID;
    }

    public String getCommodityName() {
        return CommodityName;
    }

    public void setCommodityName(String commodityName) {
        CommodityName = commodityName;
    }

    public String getCommoditySize() {
        return CommoditySize;
    }

    public void setCommoditySize(String commoditySize) {
        CommoditySize = commoditySize;
    }

    public String getCommodityPrice() {
        return CommodityPrice;
    }

    public void setCommodityPrice(String commodityPrice) {
        CommodityPrice = commodityPrice;
    }

    public String getCommodityTotalPrice() {
        return CommodityTotalPrice;
    }

    public void setCommodityTotalPrice(String commodityTotalPrice) {
        CommodityTotalPrice = commodityTotalPrice;
    }

    public String getCommodityType() {
        return CommodityType;
    }

    public void setCommodityType(String commodityType) {
        CommodityType = commodityType;
    }

    public String getCommoditySupplier() {
        return CommoditySupplier;
    }

    public void setCommoditySupplier(String commoditySupplier) {
        CommoditySupplier = commoditySupplier;
    }
}
